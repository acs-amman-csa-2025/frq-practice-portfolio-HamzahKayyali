public class Person {
    private String name;
    private int age;
    private String status;

    public Person(String na, int ag, String st){
        name = na;
        age = ag;
        status = st;
    }

    public String getName(){
        return name;
}
 public int getAge(){
        return age;
}
 public String getStatus(){
        return status;
}

}
